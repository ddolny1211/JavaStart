package metodyiKonstruktory;

public class MetricCalc {
    double metersToCentimeters(double a){
        return a*100;
    }
    double metersToMilimiters(double a){
        return a*1000;
    }
    double centimetersToMetres(double a){
        return a/100;
    }
    double milimitersToMetres(double a){
        return a/1000;
    }
}
